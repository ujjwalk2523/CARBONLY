window.addEventListener("DOMContentLoaded", () => {
  const connectBtn = document.getElementById("connectWallet");

  // Connect Wallet Button Click
  // --------------------
  // Connect Wallet
  // --------------------
  connectBtn.addEventListener("click", async () => {
    if (window.ethereum && window.ethereum.isMetaMask) {
      try {
        const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
        currentAccount = accounts[0];
        web3 = new Web3(window.ethereum);
        contract = new web3.eth.Contract(contractABI, contractAddress);

        connectBtn.innerText = currentAccount.slice(0, 6) + "..." + currentAccount.slice(-4);

        // Load dashboard info
        loadDashboard(currentAccount);
      } catch (err) {
        console.error("MetaMask error:", err);
        alert("MetaMask connection rejected.");
      }
    } else {
      alert("MetaMask not detected. Install MetaMask extension!");
    }
  });

  // Listen for account/network changes
  if (window.ethereum) {
    window.ethereum.on('accountsChanged', (accounts) => {
      if (accounts.length > 0) {
        currentAccount = accounts[0];
        connectBtn.innerText = currentAccount.slice(0, 6) + "..." + currentAccount.slice(-4);
        loadDashboard(currentAccount);
      } else {
        resetDashboard();
      }
    });

    window.ethereum.on('chainChanged', () => window.location.reload());
  }

  // Logout button
  document.getElementById("logoutBtn").addEventListener("click", () => {
    resetDashboard();
    alert("Wallet disconnected.");
  });
});

// --------------------
// Load Dashboard Data
// --------------------
async function loadDashboard(account) {
  try {
    // 1ï¸âƒ£ Load Token Balance
    const balance = await contract.methods.balanceOf(account).call();
    const decimals = await contract.methods.decimals().call();
    const symbol = await contract.methods.symbol().call();
    const formattedBalance = balance / (10 ** decimals);
    document.getElementById("XCCBalance").innerText = `${formattedBalance} ${symbol}`;

    // 2ï¸âƒ£ Fetch Projects from backend
    const res = await fetch(`http://localhost:5000/projects/${account}`);
    const projects = await res.json();

    // Total Projects
    document.getElementById("totalProjects").innerText = projects.length;

    // Total Land (sum of `area` if stored, else count projects as 1 ha each)
    const totalLand = projects.reduce((sum, p) => sum + (p.area || 1), 0);
    document.getElementById("totalLand").innerText = totalLand;

    // Total COâ‚‚ Offset (sum of `co2Offset` field if exists)
    const totalCO2 = projects.reduce((sum, p) => sum + (p.co2Offset || 0), 0);
    document.getElementById("totalCO2").innerText = totalCO2.toFixed(2);

  } catch (err) {
    console.error("Error loading dashboard:", err);
  }
}

// --------------------
// Reset Dashboard UI
// --------------------
function resetDashboard() {
  currentAccount = null;
  web3 = null;
  contract = null;

  document.getElementById("connectWallet").innerText = "Connect Wallet";
  document.getElementById("XCCBalance").innerText = "0 XCC";
  document.getElementById("totalProjects").innerText = "0";
  document.getElementById("totalLand").innerText = "0";
  document.getElementById("totalCO2").innerText = "0";
}